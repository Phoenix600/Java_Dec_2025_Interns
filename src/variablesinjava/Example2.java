package variablesinjava;

public class Example2 {

    public static void main(String[] args)
    {
        // Integral Category
        byte bNum1 = 127;
        byte bNum2;  // uninitialized
        System.out.println(bNum1);

        short sNum1 = -29000;
        short sNum2;
        System.out.println(sNum1);

        int num1 = 10090;
        int num2;
        System.out.println(num1);

        long lNum1 = 12000000L;
        long lNum2;
        System.out.println(lNum1);

        // Floating Point Category
        float fNum1 = 12.121212f;
        float fNum2;
        System.out.println(fNum1);

        double dNum1 = 12.12121212121212121;
        double dNum2;
        System.out.println(dNum1);

        // Character
        // 1,2,3,4,5,6,7,8,9,0, A-Z, a-z !@#$%^&*()
        char cSection1 = 'A';
        char cSection2;
        System.out.println(cSection1);

        // Boolean category
        boolean isEven = true;
        boolean isPrime = false;
        boolean isSingle;

        System.out.println(isEven);
        System.out.println(isPrime);

    }
}
