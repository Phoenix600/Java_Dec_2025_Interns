package variablesinjava;

public class Example1 {
    public static void main(String[] args) {
        // variables
        // syntax: data_type name = value;

        int number; // variable declaration
        int number1 = 12; // declaration and initialization

        number = 14; // assignment
        number = 13; // reassignment

        byte age = 27;  // -128 to 127
        short votes = 23000; // -32K to +32K
        int ageOfEarth = 2000000000;
        long ageUniverse = 2323839437847384L;

        float percentage = 12.1212f; // 6-7
        double pptRate = 0.456732657323465733434443;  // 15-16

        char a = 'A';  // anything which is inside single quote and there must be a single symbol
        boolean isEven = true;  // true or false;
    }
}
